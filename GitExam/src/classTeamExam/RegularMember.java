package classTeamExam;

public class RegularMember extends Member {
	private int addWorkingTime;
	private int lastGrade;
	private String cert;
	private String jobLevel;
	
	public int getAddWorkingTime() {
		return addWorkingTime;
	}
	public void setAddWorkingTime(int addWorkingTime) {
		this.addWorkingTime = addWorkingTime;
	}
	public int getLastGrade() {
		return lastGrade;
	}
	public void setLastGrade(int lastGrade) {
		this.lastGrade = lastGrade;
	}
	public String getCert() {
		return cert;
	}
	public void setCert(String cert) {
		this.cert = cert;
	}
	public String getJobLevel() {
		return jobLevel;
	}
	public void setJobLevel(String jobLevel) {
		this.jobLevel = jobLevel;
	}
	
	
}
