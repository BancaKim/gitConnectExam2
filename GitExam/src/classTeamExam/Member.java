package classTeamExam;

public class Member {
	private int id;
	private String name;
	private int joinYear;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getJoinYear() {
		return joinYear;
	}
	public void setJoinYear(int joinYear) {
		this.joinYear = joinYear;
	}
	
	
}
